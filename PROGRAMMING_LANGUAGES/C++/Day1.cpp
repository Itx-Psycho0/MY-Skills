// Day 1 -------- C++ Basics ----------------

// What is C++?
// C++ is a high-level, general-purpose programming language created by Bjarne Stroustrup as an extension of the C programming language. It supports object-oriented, procedural, and generic programming paradigms.
// C++ is widely used for system/software development, game development, real-time simulations, and performance-critical applications.

// Why C++?
// 1. Performance: C++ is known for its high performance and efficiency, making it suitable for resource-intensive applications.
// 2. Object-Oriented Programming: C++ supports OOP principles like encapsulation, inheritance, and polymorphism, allowing for modular and reusable code.
// 3. Standard Template Library (STL): C++ provides a rich set of libraries for data structures and algorithms, which can significantly speed up development.
// 4. Portability: C++ code can be compiled on various platforms with minimal changes.
// 5. Community and Ecosystem: C++ has a large and active community, with extensive resources, libraries, and tools available for developers.
// 6. Compatibility with C: C++ is compatible with C, allowing developers to use existing C libraries and codebases.
// 7. Control over System Resources: C++ provides low-level access to memory and system resources, which is essential for system programming.

// Basic Structure of a C++ Program
#include <iostream> // Preprocessor directive to include the iostream library
using namespace std; // Use the standard namespace
int main() { // Main function - entry point of the program
    cout << "Hello, World!" << endl; // Output statement
    return 0; // Return statement indicating successful execution
}
// This program prints "Hello, World!" to the console.


