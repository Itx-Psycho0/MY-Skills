// Bitwise operators

// Bitwise &
// Bitwise |
// Bitwise ^
// Bitwise ~
// Bitwise <<
// Bitwise >>

// & - 0&0 - 0


#include <iostream>
using namespace std;
int main(){
    int a = 4, b = 8;
    cout << (a&b) <<"\n";
    cout << (a|b) <<"\n";
    cout << (a^b) <<"\n";
    cout << (~a) <<"\n";
    cout << (a<<2) <<"\n";
    cout << (a>>2) <<"\n";
    return 0;

}